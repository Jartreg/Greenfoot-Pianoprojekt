/**
 * Eine schwarze Taste
 *
 * Es werden verschiedene Klassen verwendet, um Greenfoot mitteilen zu können,
 * dass schwarze Tasten immer über den weißen erscheinen sollen.
 */
public class TasteSchwarz extends Taste {
	public TasteSchwarz(Note note, String key) {
		super(note, key, "black");
	}
}
